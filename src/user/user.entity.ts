import {
  Column,
  Entity,
  PrimaryGeneratedColumn,
  CreateDateColumn,
  UpdateDateColumn,
} from 'typeorm';

@Entity()
export class User {
  @PrimaryGeneratedColumn()
  id: number;

  @Column({ length: 500 })
  name: string;

  @Column({ length: 500 })
  email: string;

  @Column('')
  passwordHash: string;

  @CreateDateColumn('')
  createdAt: string;

  @UpdateDateColumn('')
  updatedAt: string;
}
